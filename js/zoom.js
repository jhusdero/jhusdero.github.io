
            $(window).scroll(function() {
  
                var target = $('#zoomGwan').offset().top;
                var viewport = window.innerHeight;
  
                    // incr when scroll down
                var scrolling = $(document).scrollTop();
  
                if(scrolling > target - viewport /2 ) {
    
                   
                    $('#zoomGwan').addClass('animated zoomIn');
                } else {
                    $('#zoomGwan').removeClass('animated zoomIn');
                }
  
                });  

